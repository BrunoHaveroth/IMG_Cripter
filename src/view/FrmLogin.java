    private boolean verificaCadastro(){
        DAO dao = DAO.getDAO();
        ObjectSet result = dao.buscar(new Usuario(null, null));
        
        if(result.size() > 0){
            return true;
        }else{
            return false;
        }
    }
    
    private void autentica(){
        DAO dao = DAO.getDAO();
        ObjectSet result = dao.buscar(new Usuario(txtLogin.getText(), txtSenha.getText()));

        if(!txtLogin.getText().isEmpty() && !txtSenha.getText().isEmpty()){
            if(result.size() > 0){
                FrmPrincipal frmP = new FrmPrincipal();
                frmP.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Senha ou login incorretos!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos n√£o preenchidos!");
        }  
    }
